package homework6;
/*
15. Write a Java program to swap two variables.
 */

public class Programme15 {
    public static void main(String[] args) {


        int a = 10;
        int b = 20;
        int c;
        c = a;
        a = b;
        b = c;

        System.out.println("a = " + a);
        System.out.println("b = " + b);

    }
}